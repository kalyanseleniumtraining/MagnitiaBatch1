package inheritanceExamples;

public class Parent {

	// add two numbers
	public void add() {
		int a = 1;
		int b = 2;
		int c = a + b;
		System.out.println(c);
	}

	// sub two numbers

	public void sub() {
		int a = 1;
		int b = 2;
		int c = b - a;
		System.out.println(c);
	}

}
